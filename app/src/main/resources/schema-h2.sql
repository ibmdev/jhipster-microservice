DROP TABLE IF EXISTS User;
CREATE TABLE User (
    id int NOT NULL AUTO_INCREMENT,
    lastName varchar(255) NOT NULL,
    firstName varchar(255),
    email varchar(255),
    PRIMARY KEY (id)
);
DROP TABLE IF EXISTS JOURNAL_TECHNIQUE;
CREATE TABLE JOURNAL_TECHNIQUE
(
    ID_OPERATION BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    LB_OPERATION VARCHAR(100) NOT NULL,
    TYPE_OPERATION VARCHAR(20) NOT NULL,
    DT_OPERATION TIMESTAMP NOT NULL,
    ID_CORRELATION VARCHAR(100),
    DT_RETOUR_OPERATION TIMESTAMP,
    STATUT SMALLINT NOT NULL,
    ID_DEMANDE VARCHAR(20),
    CD_RETOUR VARCHAR(10) NOT NULL,
    LB_RETOUR VARCHAR(250),
    FLUX_ENTREE BLOB(102400),
    CD_APPLICATION VARCHAR(2),
    FLUX_RETOUR BLOB(512000),
    PRIMARY KEY (ID_OPERATION)
);
